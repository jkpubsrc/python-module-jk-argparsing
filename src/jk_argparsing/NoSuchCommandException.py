class NoSuchCommandException(Exception):

	################################################################################################################################
	## Constants
	################################################################################################################################

	################################################################################################################################
	## Constructor
	################################################################################################################################

	#
	# Constructor method.
	#
	@jk_typing.checkFunctionSignature()
	def __init__(self,
			cmd:str,
			availableCmds:list[str],
		):
		super().__init__(f"Unknown command: \"{cmd}\"")
		self.cmd = cmd
		self.availableCmds = availableCmds
		self.extraValues = {
			"cmd": cmd,
			"availableCmds": availableCmds,
		}
	#

	################################################################################################################################
	## Public Properties
	################################################################################################################################

